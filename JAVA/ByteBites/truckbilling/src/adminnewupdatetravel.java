    private void PlateBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PlateBoxActionPerformed
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/billing", "root", "");
            PreparedStatement state = (PreparedStatement) con.prepareStatement("SELECT PlateNumber from truckdet ");
            ResultSet rs = state.executeQuery();

            while (rs.next()) {

                String pnumber = rs.getString("PlateNumber");
                PlateBox.addItem(pnumber);
            }
        } catch (SQLException ex) {
            Logger.getLogger(adminnewupdatetravel.class.getName()).log(Level.SEVERE, null, ex);  
        
        
        }
    }//GEN-LAST:event_PlateBoxActionPerformed

