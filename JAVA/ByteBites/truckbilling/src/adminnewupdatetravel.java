    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String dates = Date.getText();
        String control = ctrl.getText();
        String plate = PlateBox.getSelectedItem().toString();
        String tto = TravelTo.getText();
        String tfrom = TravelFrom.getText();
        int km = Integer.parseInt(Kilometers.getText());
        int bcarried = Integer.parseInt(BagsCarried.getText());
        int bprice = Integer.parseInt(PriceBag.getText());
        int tprice = Integer.parseInt(TotalPrice.getText());
        
        try{
         Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/billing","root","");
        PreparedStatement pst = (PreparedStatement) con.prepareStatement("INSERT INTO traveldet(date,CtrlNo, PlateNo, Travelto, Travelfrom, Km, PiecesBag, PriceBag, ShippingTotal )Values(?,?,?,?,?,?,?,?,?)"); 
        
            pst.setString(1,dates);
            pst.setString(2,control);
            pst.setString(3,plate);
            pst.setString(4,tto);   
            pst.setString(5,tfrom);
            pst.setInt(6,km);
            pst.setInt(7,bcarried);
            pst.setInt(8,bprice);
            pst.setInt(9,tprice);
            
            int k = pst.executeUpdate();
            
            if(k==1){
             Date.setText("");  
             ctrl.setText("");
             TravelTo.setText("");
             TravelFrom.setText("");
             Kilometers.setText("");
             BagsCarried.setText("");
             PriceBag.setText("");
             TravelFrom.setText("");
             TotalPrice.setText("");
             
             JOptionPane.showMessageDialog(null, "Travel Details added");
            }
        
        }catch(SQLException ex) {
          Logger.getLogger(adminnewupdatetravel.class.getName()).log(Level.SEVERE, null, ex);  
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int km = Integer.parseInt(Kilometers.getText());
        int bcarried = Integer.parseInt(BagsCarried.getText());
        int bprice = Integer.parseInt(PriceBag.getText());
        
 
        
        if(km>1){
               if(km<=20){
                   int kmp = 1500;
                   int total = bprice*bcarried+kmp;
                   TotalPrice.setText(String.valueOf(total));
                  
               }else{
                   int kmp = (1500+(km-20)*30);
                   int total = bprice*bcarried+kmp;       
                   TotalPrice.setText(String.valueOf(total));
               } 
            }

    }//GEN-LAST:event_jButton3ActionPerformed

